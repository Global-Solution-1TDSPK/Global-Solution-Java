package br.com.fiap.entity;

public class Triagem {

	private String motiVisita;
	private String tomMedicamento;
	private String condPreExist;
	private String sintEspecifico;
	private String contProxDoenInfec;
	private String viagemSurto;
	public String getMotiVisita() {
		return motiVisita;
	}
	public void setMotiVisita(String motiVisita) {
		this.motiVisita = motiVisita;
	}
	public String getTomMedicamento() {
		return tomMedicamento;
	}
	public void setTomMedicamento(String tomMedicamento) {
		this.tomMedicamento = tomMedicamento;
	}
	public String getCondPreExist() {
		return condPreExist;
	}
	public void setCondPreExist(String condPreExist) {
		this.condPreExist = condPreExist;
	}
	public String getSintEspecifico() {
		return sintEspecifico;
	}
	public void setSintEspecifico(String sintEspecifico) {
		this.sintEspecifico = sintEspecifico;
	}
	public String getContProxDoenInfec() {
		return contProxDoenInfec;
	}
	public void setContProxDoenInfec(String contProxDoenInfec) {
		this.contProxDoenInfec = contProxDoenInfec;
	}
	public String getViagemSurto() {
		return viagemSurto;
	}
	public void setViagemSurto(String viagemSurto) {
		this.viagemSurto = viagemSurto;
	}
	
}
